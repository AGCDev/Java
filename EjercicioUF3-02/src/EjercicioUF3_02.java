
public class EjercicioUF3_02 {

	public static void main(String[] args) {
		
		System.out.println("/////////// Bucle For \\\\\\\\\\\\\\\\\\\\\\\\");
		
		for (int i = 100; i >= 10; i-=10) {
			System.out.println(i);
		}
		
		
		System.out.println();
		System.out.println();
		
		System.out.println("/////////// Bucle While \\\\\\\\\\\\\\\\\\\\\\");
		
		int num = 100;
		
		while (num >= 10) {
			System.out.println(num);
			num -= 10; 
		}
	}
}
